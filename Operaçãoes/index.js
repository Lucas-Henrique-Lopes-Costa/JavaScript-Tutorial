// Adição
var sum = 10 + 0;
console.log(sum)

sum += 10;
console.log(sum)

// Subtração
var diferrence = 10 - 5;
console.log(diferrence)

diferrence -= 10;
console.log(diferrence)

// Multiplicação
var product = 8 * 10;
console.log(product)

product *= 10;
console.log(product)

// Divição
var quotient = 66 / 33;
console.log(quotient)

quotient /= 2;
console.log(quotient)

// Incrementando números
var increment = quotient + 1;
console.log(increment)

// Diminuindo número
increment--;
console.log(increment)

// Resto da divisão
var remainder;
remainder = 11 % 3;
console.log(remainder)
